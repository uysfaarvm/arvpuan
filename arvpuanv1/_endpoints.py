def get_endpoints(site: str) -> tuple:
    """
    (base_url, points_url, referer_url) döndürür.
    site='a' → birincil, site='b' → ikincil
    """
    if site == "b":
        return _d(_H2), _d(_U2), _d(_R2)
    return _d(_H1), _d(_U1), _d(_R1)
